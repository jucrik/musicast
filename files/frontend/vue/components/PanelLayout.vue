  homeUrl: {
    type: String,
    required: true,
  },
  profileUrl: {
    type: String,
    required: true,
  },
  adminUrl: {
    type: String,
    required: true
  },
  logoutUrl: {
    type: String,
    required: true
  },
  showAdmin: {
