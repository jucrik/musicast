    default: false
  },
  version: {
    type: String,
    required: true
  },
  platform: {
      type: String,
      required: true
  }
});

const slots = useSlots();

const handleSidebar = () => {
    if (slots.sidebar) {
        document.body.classList.add('has-sidebar');
    } else {
        document.body.classList.remove('has-sidebar');
    }
}

watch(
    () => slots.sidebar,
    handleSidebar,
    {
        immediate: true
    }
);
</script>
